public class Operators {
  public static void main(String[] args) {
    // Math
    /**
     * +: sum
     * -: subtraction
     * *: multiplication
     * %: Module or rest of the partition
    */

    // Simplifying
    /**
     *    Simple form        Complex form
     *      x += y            x = x + y
     *      x -= y            x = x - y
     *      x *= y            x = x * y
     *      x /= y            x = x / y
     *       x++              x = x + 1
     *       x--              x = x - 1
    */

    // Logical
    /**
     *    Operator           Meaning
     *       !                 NOT
     *       >             Greater than
     *       <              Less than
     *      ==                equal
     *      &&                 AND
     *      ||                  OR
     *      ^                  XOR
     *      >=        Greater or equal than   
     *      <=          Less or equal than
     *      !=              different 
    */
  }
}
